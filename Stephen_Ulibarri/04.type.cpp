#include <iostream>
using namespace std;

int main()
{
    char a;
    int b;
    short c;
    bool d;
    float e;
    double f;
    long double g;

    cout << "sizeof(char)=" << sizeof(a) << endl;
    cout << "sizeof(int)=" << sizeof(b) << endl;
    cout << "sizeof(short)=" << sizeof(c) << endl;
    cout << "sizeof(bool)=" << sizeof(d) << endl;
    cout << "sizeof(float)=" << sizeof(e) << endl;
    cout << "sizeof(double)=" << sizeof(f) << endl;
    cout << "sizeof(long double)=" << sizeof(g) << endl;
    
    return 0;
}
