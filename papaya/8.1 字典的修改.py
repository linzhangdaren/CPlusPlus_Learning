手机 = {
    "品牌": "小米",
    "型号": "xiaomi10",
    "Rom": "128G",
    "Ram": ["4G", "6G", "8G"],
}

# 添加参数
手机["颜色"] = ["黑色", "白色"]
print(手机["颜色"][0])  # 输出黑色
# 查询有多少种颜色
print(len(手机["颜色"]))  # 输出2

# 修改参数
手机["型号"] = "小米10"
print(手机["型号"])

# del删除参数
del 手机["Ram"][0]
print(手机["Ram"])

# pop删除参数
# 手机.pop("型号")#全部删除
# 手机.popitem()#删除最后一个参数
手机.pop("Ram", "8G")  # 删除指定参数
print(手机["Rom"])

# 清除所有字典的参数
手机.clear()
print(手机)
