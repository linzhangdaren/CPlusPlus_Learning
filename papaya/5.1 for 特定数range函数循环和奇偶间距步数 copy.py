# for 循环
早餐 = ["蛋饼", "三明治", "汉堡", "饭团"]
# 循环遍历到下标2结束
for i in range(2):
    print(早餐[i])

# range是个函数,可以生成一个整数序列,range(2)生成的序列是[0,1],range(3)生成的序列是[0,1,2]
# range(2)的意思是从0开始,到1结束,生成的序列是[0,1]
# range(3)的意思是从0开始,到2结束,生成的序列是[0,1,2]
# 所以range(2)的意思是遍历到下标2结束,即只打印出早餐列表的前两个元素

for i in range(0, 10, 2):  # 步长为2,从0开始,到9结束,生成的序列是[0,2,4,6,8]
    print(i)
