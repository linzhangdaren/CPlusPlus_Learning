# 字符串也可以采用清单的常用操作方式
静夜思 = "床前明月光"
print(len(静夜思))  # 5
print(静夜思[0])  # 床
print(静夜思[0:])  # 床前明月光

# 字符串转换清单
print(list(静夜思))  # ['床', '前', '明', '月', '光']

# 日期字符串按照/分割转换为清单
日期 = "2025/1/18"
print(日期.split("/"))  # ['2025', '1', '18']

# 清单转换为字符串
学生 = ["张三", "李四", "王五"]
print("/".join(学生))  # 2025/1/18 ""里面也可以啥也不写直接把清单转为字符串方便显示阅读
