# 判断布尔值
a = 1
b = 2
print(a == b)  # False
print(a != b)  # True
print(a < b)  # True
print(a > b)  # False
print(a <= b)  # True
print(a >= b)  # False

# 与或非
# 非 (翻转的意思 反向)
print(not True)  # False
print(not False)  # True
# 与
print(True and True)  # True
print(True and False)  # False
# 或
print(True or False)  # True
print(True or True or False)  # True
