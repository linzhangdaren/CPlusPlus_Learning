手机1 = {
    "品牌": "小米",
    "型号": "小米10",
    "颜色": "灰色",
    "内存": "64G",
    "存储": "128G",
}

手机2 = {
    "品牌": "小米",
    "型号": "小米12",
    "颜色": "白色",
    "内存": "128G",
    "存储": "1T",
}

库存 = [手机1, 手机2]
查询 = input("请输入手机的品牌:")
if 查询 in str(库存):
    print("该型号手机在库存中")
    for 手机 in 库存:
        if 手机["品牌"] in 查询:
            print(
                {
                    f"目前库中有{手机['型号']}，颜色为{手机['颜色']}，内存为{手机['内存']}，存储为{手机['存储']}"
                }
            )
else:
    print("该型号手机不在库存中")
