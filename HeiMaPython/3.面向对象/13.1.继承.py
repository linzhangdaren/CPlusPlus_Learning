#子类可以继承所有父类的属性和方法
class 爸爸():
    def __init__(self,姓名,年龄):
        self.姓名=姓名
        self.年龄=年龄
    
    def 打招呼(self):
        print(f"我是{self.姓名},我{self.年龄}岁了。")
        
class 儿子(爸爸):
    def __init__(self,姓名,年龄,学校):
        super().__init__(姓名,年龄)
