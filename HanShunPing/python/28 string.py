# 引号的使用

# 1. 输出  A:"xxx" 第一人称的语句
# 用单引号括住整个语句
a = '我说:"你好，世界"'
print(a)  # 输出：我说:"你好，世界"

# 2. 输出 B:'xxx' 第一人称的语句
# 用双引号括住整个语句
b = "我说:'你好，世界'"
print(b)  # 输出：我说:'你好，世界'

# 3. 输出a+b
# 字符串拼接
c = a + b  # "a"+"b" 也可以
print(c)  # 输出：我说:"你好，世界"我说:'你好，世界'

# 4.原格式化输出用""" """"
# 适合大量复制粘贴使用 三个引号用之间要有空格
d = """ A:"xxx" B:'xxx' """
print(d)  # 输出： A:"xxx" B:'xxx'
