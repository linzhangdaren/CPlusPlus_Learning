# 交互模式 interactive mode
# 可以直接在终端输入代码，回车后立即执行，并显示结果

# 第一种交互模式:python解释器

# 1.打开CMD，输入python，进入交互模式
# 2.输入print('hello world')，回车，输出hello world
# 3.或者输入 1+1，回车，输出2
# 3.输入exit()，退出交互模式

# 第二种交互模式:ipython解释器(比py更智能有AI补全功能)
# 打开CMD，输入ipython，进入交互模式
# 如果提示错误说明没有安装ipython，需要安装 输入命令: pip install ipython
# 如果慢可以换阿里云源: pip install ipython -i https://mirrors.aliyun.com/pypi/simple/
# pip加速网址百度就可以找到加到 -i后面就行了

