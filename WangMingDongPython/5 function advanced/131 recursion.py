# 阶乘问题
# 4! = 4 * 3 * 2 * 1 = 24  等价于4*3!
# 5! = 5 * 4 * 3 * 2 * 1 = 120  等价于5*4!

# 推导过程
# 4! = 4 * 3!
# 3! = 3 * 2!
# 2! = 2 * 1!
# 1! = 1(*0!)不能0,要1


# 递归函数 不用for循环
def recursion(n):
    if n > 1:
        return n * recursion(n - 1)
    else:
        return 1


# 4*3*2*1*0(不能0,要1)

print(recursion(4))
