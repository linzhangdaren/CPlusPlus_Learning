# 1-4种数据类型的使用场景

# 1.列表
# 储存多个相同数据类型的时候
# 如年龄,姓名,电话号码多个qq号等(增删改查)

# 2.元组 不推荐
# 不能修改的数据类型,优先考虑使用元组
# 存款利率

# 3.集合 非常少
# 去重时候其他基本不使用
# 一人投多票 算成1票


# 4.字典 经常使用
# 存多个数据 多个类型 为一整体的
# 班级 班号 班主任 学生人数 学生姓名 学生学号
# class _info:
#     {
#         "name": "三年二班",
#         "teacher": "王老师",
#         "student_num": 10,
#     }
