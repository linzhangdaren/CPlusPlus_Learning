# 比较运算符 comparison operator

a = 1
b = 2

print(a == b)  # True
print(a != b)  # False
print(a > b)  # False
print(a >= b)  # False
print(a < b)  # True
print(a <= b)  # True


# 等于等于 ==
if a == b:
    print("a等于b")
else:
    print("a不等于b")

# 不等于！=
if a != b:
    print("a不等于b")
else:
    print("a等于b")

# 大于 >
if a > b:
    print("a大于b")
else:
    print("a不大于b")

# 大于等于 >=
if a >= b:
    print("a大于或等于b")
else:
    print("a不大于或等于b")

# 小于 <
if a < b:
    print("a小于b")
else:
    print("a不小于b")

# 小于等于 <=
if a <= b:
    print("a小于或等于b")
else:
    print("a不小于或等于b")
