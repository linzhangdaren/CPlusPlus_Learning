#include <iostream>
using namespace std;

int main()
{
    int scores[5] = {1, 2, 3, 4, 5};

    cout << scores[0] << endl; // 结果值是1
    cout << scores[1] << endl; // 结果值是2
    cout << scores[2] << endl; // 结果值是3
    cout << scores[3] << endl; // 结果值是4
    cout << scores[4] << endl; // 结果值是5

    return 0;
}