#include <iostream>
using namespace std;

int main()
{
    // 定义一个char类型的变量
    char num = 'a';
    // 以10进制输出num
    cout << "char num = \'a\'" << endl;
    cout << "以10进制输出num的值int(num)" << endl;
    cout << num << "对应的十进制数是:" << int(num) << endl;
    cout<<'a'+1<<endl;
    cout<<char(99)<<endl;
    return 0;
}